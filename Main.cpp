#include <iostream>
using namespace std;

int main()
{
  int a = 8,b = 3;
  
  //Addition operators
  cout <<" a+b = " << ( a+b) << endl;
  
  //Subtraction operator
  cout <<" a-b = " << (a-b) << endl;
  
  //multiplication operator
  cout <<"a*b = " << (a*b) << endl;
  
  //Division operator
  cout <<"a/b =" << (a/b) << endl;
  
  //Modulo operator 
  cout <<"a%b =" << (a%b) << endl;
  return 0;
}


